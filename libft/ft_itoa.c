#include "libft.h"

static int	int_len(int n)
{
	int	len;

	len = 0;
	if (n <= 0)
		len = 1;
	while (n != 0)
	{
		n = n / 10;
		len++;
	}
	return (len);
}

char	*ft_itoa(int n)
{
	char	*str;
	int		len;
	long	temp_n;

	temp_n = n;
	len = int_len(n);
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	str[len] = '\0';
	if (temp_n == 0)
	{
		str[0] = '0';
	}
	if (temp_n < 0)
	{
		str[0] = '-';
		temp_n = -temp_n;
	}
	while (temp_n > 0)
	{
		str[--len] = (temp_n % 10) + '0';
		temp_n = temp_n / 10;
	}
	return (str);
}
